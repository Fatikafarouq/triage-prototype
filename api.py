import uuid
from datetime import datetime
from typing import Optional

from fastapi import FastAPI, HTTPException
from fastapi.middleware.cors import CORSMiddleware
from pydantic import BaseModel

from vet_agent import VetAgent
from vet_classifier import classify as triage_classify
from vet_sop_knowledge import CLINIC_NAME, CLINIC_PHONE, EMERGENCY_LINE


# ─────────────────────────────────────────────────────────────
#  APP
# ─────────────────────────────────────────────────────────────

app = FastAPI(
    title="VetDesk AI API",
    description=(
        f"REST API for the VetDesk AI veterinary receptionist agent.\n\n"
        f"Handles triage classification, SOP-based Q&A, and appointment booking.\n\n"
        f"Emergency line: **{EMERGENCY_LINE}**"
    ),
    version="1.0.0",
)

# Allow any frontend or external service to call this API.
# In production: replace "*" with your allowed domains.
app.add_middleware(
    CORSMiddleware,
    allow_origins=["*"],
    allow_methods=["*"],
    allow_headers=["*"],
)


# ─────────────────────────────────────────────────────────────
#  SESSION STORE
#  Each caller gets a session_id so multi-turn booking works.
#  In production: use Redis or a database instead of a dict.
# ─────────────────────────────────────────────────────────────

SESSIONS: dict[str, VetAgent] = {}


def get_or_create_session(session_id: Optional[str]) -> tuple[str, VetAgent]:
    """Return an existing agent session or create a new one."""
    sid = session_id or str(uuid.uuid4())
    if sid not in SESSIONS:
        SESSIONS[sid] = VetAgent()
    return sid, SESSIONS[sid]


# ─────────────────────────────────────────────────────────────
#  REQUEST / RESPONSE MODELS
#  Pydantic models define what the API accepts and returns.
#  FastAPI generates /docs automatically from these.
# ─────────────────────────────────────────────────────────────

class ChatRequest(BaseModel):
    message: str
    session_id: Optional[str] = None


class ChatResponse(BaseModel):
    session_id: str
    reply: str
    triage_level: str       # EMERGENCY / MODERATE / ROUTINE / UNKNOWN
    booking_active: bool    # True if the agent is mid-booking-flow
    timestamp: str


class TriageRequest(BaseModel):
    message: str


class TriageResponse(BaseModel):
    message: str
    triage_level: str       # EMERGENCY / MODERATE / ROUTINE / UNKNOWN
    method: str             # keyword / pattern / contextual / none
    normalised: str         # cleaned version of input (useful for debugging)
    timestamp: str


# ─────────────────────────────────────────────────────────────
#  ENDPOINTS
# ─────────────────────────────────────────────────────────────

@app.get("/health", tags=["System"])
def health_check():
    """
    Check that the API is running.

    Use this for uptime monitoring or to verify your connection
    before sending patient messages.
    """
    return {
        "status":          "ok",
        "clinic":          CLINIC_NAME,
        "emergency_line":  EMERGENCY_LINE,
        "sessions_active": len(SESSIONS),
        "timestamp":       datetime.utcnow().isoformat(),
    }


@app.post("/chat", response_model=ChatResponse, tags=["Chat"])
def chat(request: ChatRequest):
    """
    Main endpoint — send a message, get a full agent reply.

    This is the endpoint your clinic software, WhatsApp bot,
    or mobile app should call for every owner message.

    **session_id** ties messages together so multi-turn
    booking flows work correctly. Generate one UUID per user
    session and reuse it throughout that conversation.

    If you omit session_id, the API creates one and returns it —
    save it from the first response and send it back each time.
    """
    if not request.message.strip():
        raise HTTPException(status_code=400, detail="Message cannot be empty.")

    session_id, agent = get_or_create_session(request.session_id)

    triage = triage_classify(request.message)
    reply  = agent.respond(request.message)

    return ChatResponse(
        session_id=session_id,
        reply=reply,
        triage_level=triage["classification"],
        booking_active=agent.booking.active,
        timestamp=datetime.utcnow().isoformat(),
    )


@app.post("/triage", response_model=TriageResponse, tags=["Triage"])
def triage_only(request: TriageRequest):
    """
    Triage classification only — no RAG, no booking logic.

    Use this when you only need the severity level, not a full reply.
    It is faster and cheaper to call than /chat.

    **Useful for:**
    - Auto-flagging urgent messages in a clinic dashboard
    - Routing incoming calls to on-call staff
    - Filtering a message queue by severity before human review
    """
    if not request.message.strip():
        raise HTTPException(status_code=400, detail="Message cannot be empty.")

    result = triage_classify(request.message)

    return TriageResponse(
        message=request.message,
        triage_level=result["classification"],
        method=result["method"],
        normalised=result["normalised"],
        timestamp=datetime.utcnow().isoformat(),
    )


@app.delete("/session/{session_id}", tags=["System"])
def clear_session(session_id: str):
    """
    Clear a session and its booking state.

    Call this when a conversation ends to free memory.
    In a production system this would be handled automatically
    by a session TTL in Redis.
    """
    if session_id not in SESSIONS:
        raise HTTPException(status_code=404, detail="Session not found.")
    del SESSIONS[session_id]
    return {
        "status":     "cleared",
        "session_id": session_id,
        "timestamp":  datetime.utcnow().isoformat(),
    }


