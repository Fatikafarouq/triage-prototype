# ─────────────────────────────────────────────────────────────
#  TEST SUITE — runs without starting the HTTP server
# ─────────────────────────────────────────────────────────────

def run_api_tests():
    """Tests the API logic directly (no HTTP server needed)."""

    print(f"\n{'=' * 62}")
    print("  VetDesk AI — Phase 5 API Test Suite")
    print(f"{'=' * 62}")

    agent = VetAgent()
    passed = failed = 0
    failures = []

    test_cases = [
        # (message, expected_fragment_in_reply, expected_triage)
        ("my dog ate chocolate",               "emergency",   "EMERGENCY"),
        ("my cat cant breathe",                "emergency",   "EMERGENCY"),
        ("what are your hours",                "Monday",      "ROUTINE"),
        ("are you open on sundays",            "closed",      "ROUTINE"),
        ("how much is a rabies vaccine",       "15",          "ROUTINE"),
        ("how much does it cost to spay",      "200",         "ROUTINE"),
        ("do you treat rabbits",               "rabbit",      "ROUTINE"),
        ("what services do you offer",         "Consultation","UNKNOWN"),
        ("my rabbit hasnt eaten today",        "today",       "MODERATE"),
        ("my dog has been vomiting 3 times",   "vet",         "MODERATE"),
        ("book an appointment",                "book",        "ROUTINE"),
        ("should i fast my rabbit before surgery", "NOT",     "UNKNOWN"),
    ]

    for message, expected_fragment, expected_triage in test_cases:
        triage = triage_classify(message)
        reply  = agent.respond(message)
        agent.booking.reset()

        reply_ok  = expected_fragment.lower() in reply.lower()
        triage_ok = triage["classification"] == expected_triage
        ok = reply_ok and triage_ok

        tri_color = "\033[91m" if triage["classification"] == "EMERGENCY" else \
                    "\033[93m" if triage["classification"] == "MODERATE"  else \
                    "\033[92m"
        sym = "\033[92m✓\033[0m" if ok else "\033[91m✗\033[0m"

        print(
            f"  {sym}  "
            f"{tri_color}[{triage['classification']:9}]\033[0m  "
            f"{message[:48]}"
        )

        if ok:
            passed += 1
        else:
            failed += 1
            tag = "" if reply_ok else f" [reply missing '{expected_fragment}']"
            tag += "" if triage_ok else f" [triage: got {triage['classification']}, expected {expected_triage}]"
            failures.append((message, tag, reply[:90]))

    print(f"\n  Results: {passed}/{len(test_cases)} passed")
    if failures:
        print("\n  Failed cases:")
        for msg, tag, got in failures:
            print(f"    {tag}")
            print(f"    Query: '{msg}'")
            print(f"    Reply: '{got}...'")

    print(f"{'=' * 62}")
    print(INTEGRATION_GUIDE)

    return passed, failed


# ─────────────────────────────────────────────────────────────
